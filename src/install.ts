export const LAUNCHD_LABEL = 'com.imgtokenx.proxy';
export const MCP_SERVER_NAME = 'imgtokenx-recover';

export interface InstallPlan {
  home: string;
  repoRoot: string;
  nodePath: string;
  cliPath: string;
  port: number;
  baseUrl: string;
  openCodeBaseUrl: string;
  openCodeConfigPath: string;
  imgtokenxDir: string;
  openCodeStatePath: string;
  envPath: string;
  /** Compatibility name: zshrcPath/zshrcBlock may target .bash_profile. */
  zshrcPath: string;
  launchAgentPath: string;
  outLog: string;
  errLog: string;
  plist: string;
  envSh: string;
  zshrcBlock: string;
  mcpCommands: string[];
  opencodeMcp: Record<string, unknown>;
}

interface BuildPlanOptions {
  home?: string;
  repoRoot?: string;
  nodePath?: string;
  port?: number;
  platform?: string;
  shell?: string;
  /** Explicit OpenCode config path for embedders/tests. Defaults to
   *  OPENCODE_CONFIG, then the active global JSON/JSONC file. */
  openCodeConfigPath?: string;
}

export interface InstallOptions extends BuildPlanOptions {
  dryRun?: boolean;
  skipMcp?: boolean;
  /** Process runner override for deterministic embedders/tests. */
  spawnSync?: (command: string, args: string[], options: {
    encoding: 'utf8';
    stdio: ['ignore', 'pipe', 'pipe'];
  }) => { status: number | null; error?: Error; stderr?: string | Buffer | null; stdout?: string | Buffer | null };
}

export interface InstallResult {
  plan: InstallPlan;
  actions: string[];
}

